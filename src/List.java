import java.util.*;


public class List {

	public static void main(String[] args) {
		
		int[] list = {3,7,10,14,16};
		
	
		for(int i=0;i<list.length;i++) {
			
		
		if(list[i]>5) {
			System.out.println(list[i]+"is greater than 5");
		}
		else  {
			System.out.println(list[i]+"is lesser than 5");
		}
		
		}
	}
	
  }

 
